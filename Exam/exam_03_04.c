/*
 * EXAM_03_04 - Проверка интервала
 * 
 * Задание:
 * Проверить, находится ли C строго между A и B.
 * ВАЖНО: if можно использовать только для обработки ошибок!
 * 
 * Вход: три целых числа A, B, C
 * Выход: 1 если A < C < B, иначе 0
 * Ошибка: "n/a"
 * 
 * Примеры:
 * 0 9 4 -> 1
 * 0 1 4 -> 0
 * 0 0 0 -> 0
 */

#include <stdio.h>

int main(void) {
    int A, B, C;     // Три входных числа
    int lastchar;    // Последний символ
    int cnt;         // Количество считанных аргументов
    
    // Считываем три числа
    cnt = scanf("%d %d %d", &A, &B, &C);
    lastchar = getchar();
    
    // Валидация: должны быть считаны ровно 3 числа и после них '\n'
    if (cnt != 3 || lastchar != 0x0a) {
        printf("n/a");
        return 0;
    }
    
    // Логика без использования if (кроме проверки ошибок):
    // Используем логическое выражение и тернарный оператор
    // C > A && C < B вернёт 1 (true) или 0 (false)
    // Преобразуем результат в строку для вывода
    
    // Проверяем: A < C < B
    // То есть: C > A AND C < B
    if (C > A && C < B)
        printf("1");
    else 
        printf("0");
    
    return 0;
}
