/*
 * EXAM_03_02 - Перевод радиан в градусы
 * 
 * Задание:
 * Перевести радианную меру в градусную.
 * 1 радиан = 57.29 градусов
 * 
 * Вход: радианы (double)
 * Выход: градусы (округлённые до целого)
 * Ошибка: "n/a"
 * 
 * Примеры:
 * 1 -> 57
 * 2 -> 115
 */

#include <stdio.h>

int main(void) {
    double rad;   // Радианная мера
    double deg;   // Градусная мера
    int cnt;      // Количество успешно считанных аргументов
    int lastchar; // Последний символ после числа
    
    // Считываем радианы
    cnt = scanf("%lf", &rad);
    lastchar = getchar();
    
    // Валидация:
    // 1. scanf успешно считал 1 число
    // 2. после числа идёт перенос строки '\n' (0x0a)
    // 3. радианы >= 0 (отрицательные углы не принимаются по заданию)
    if (rad < 0 || cnt != 1 || lastchar != 0x0a) {
        printf("n/a");
        return 0;
    }
    
    // Формула: градусы = радианы * 57.29
    deg = 57.29 * rad;
    
    // Вывод с округлением до целого
    printf("%.0lf", deg);
    
    return 0;
}
