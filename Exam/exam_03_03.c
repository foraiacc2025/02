/*
 * EXAM_03_03 - Перестановка крайних цифр
 * 
 * Задание:
 * Поменять местами первую и последнюю цифры трёхзначного числа.
 * С учётом ведущих нулей и знака.
 * 
 * Вход: трёхзначное целое число
 * Выход: число с переставленными крайними цифрами
 * Ошибка: "n/a"
 * 
 * Примеры:
 * 159 -> 951
 * 120 -> 021
 * -123 -> -321
 */

#include <stdio.h>
#include <math.h>

int main(void) {
    int x;            // Исходное число
    int tp, sp, fp;   // Third Position, Second Position, First Position (цифры)
    int minus = 0;    // Флаг отрицательного числа
    int lastchar;     // Последний символ
    int cnt;          // Количество считанных аргументов
    
    // Считываем число
    cnt = scanf("%d", &x);
    lastchar = getchar();
    
    // Проверяем знак и берём модуль
    if (x < 0) {
        minus = 1;
        x = -x;  // Делаем положительным для удобства работы
    }
    
    // Можно было бы использовать fabs, но для int проще так:
    // x = fabs(x); - это для double
    
    // Валидация ввода
    if (cnt != 1 || lastchar != 0x0a) {
        printf("n/a");
        return 0;
    }
    
    // Извлекаем цифры:
    // tp - последняя цифра (единицы)
    tp = x % 10;
    x = x / 10;
    
    // sp - средняя цифра (десятки)
    sp = x % 10;
    
    // fp - первая цифра (сотни)
    fp = x / 10;
    
    // Выводим с учётом знака
    // Порядок: tp (была последней) sp (средняя) fp (была первой)
    if (minus == 1) {
        printf("-%d%d%d", tp, sp, fp);
    } else {
        printf("%d%d%d", tp, sp, fp);
    }
    
    return 0;
}
