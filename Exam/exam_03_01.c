/*
 * EXAM_03_01 - Масса платинового шара
 * 
 * Задание:
 * Вычислить массу шара из платины по радиусу.
 * Плотность платины = 21500 кг/м³
 * Формула объема: V = (4/3) * π * R³
 * Формула массы: m = ρ * V
 * 
 * Вход: радиус R (double) в метрах
 * Выход: масса m (округлённая до целого)
 * Ошибка: "n/a"
 * 
 * Примеры:
 * 1 -> 90059
 * 10 -> 90058989
 */

#include <stdio.h>
#include <math.h>

#ifndef M_PI
#define M_PI 3.14159265358979323846
#endif

int main(void) {
    double R;     // Радиус шара в метрах
    char c;       // Для проверки конца ввода
    
    // Считываем радиус и сразу проверяем что после него идёт перенос строки
    scanf("%lf%c", &R, &c);
    
    // Валидация: радиус >= 0 и после него только '\n'
    if (R >= 0 && c == 0x0a) {
        // Плотность платины = 21500 кг/м³
        // Объём шара V = (4/3) * π * R³
        // Масса m = ρ * V = 21500 * (4/3) * π * R³
        double m = 21500.0 * (4.0 / 3.0) * M_PI * pow(R, 3);
        
        // Округление уже встроено в %.0lf (округление к ближайшему целому)
        printf("%.0lf", m);
    } else {
        // Некорректный ввод
        printf("n/a");
        return 0;
    }
    
    return 0;
}
